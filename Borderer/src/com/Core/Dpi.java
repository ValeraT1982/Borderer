package com.Core;

public class Dpi {
    private int widthDpi;
    private int heightDpi;

    public Dpi(int widthDpi, int heightDpi) {
        this.widthDpi = widthDpi;
        this.heightDpi = heightDpi;
    }

    public int getWidthDpi() {
        return widthDpi;
    }


    public int getHeightDpi() {
        return heightDpi;
    }
}
